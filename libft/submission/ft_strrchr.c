#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	char c1;
	char *s1;
	char *s2;

	s1 = (char *)s;
	s2 = 0;
	c1 = (char)c;
	while (*s1++ != '\0')
	{
		if (*s1 == c1)
		{
			s2 = s1;
		}
	}
	return (s2 != 0 ? s2 : NULL);
}
